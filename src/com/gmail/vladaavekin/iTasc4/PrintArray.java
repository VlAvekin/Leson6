package com.gmail.vladaavekin.iTasc4;

public class PrintArray {

    public static void Array(final int[] Array){

        for (int i : Array) {
            System.out.print(i + "\t");
        }
        System.out.println();
    }

    public static void Array(final double[] Array){

        for (double i : Array) {
            System.out.print(i + "\t");
        }
        System.out.println();
    }
}
